multiline_string = """This is a multiline string in Python.
It can span multiple lines without the need for newline characters."""
print(multiline_string)

# Using triple quotes to create a multiline string
multiline_string_triple = """This is another way to create a multiline string. You can use triple quotes to include line breaks and preserve the formatting."""
print(multiline_string_triple)

multiline_string_f = f"""This is a multiline string using f-strings. You can include variables and expressions inside the string. For example, the value of 2 + 2 is {2 + 2}."""
print(multiline_string_f)

single_quote_multiline = '''This is a multiline string using single quotes. 
It works the same way as triple double quotes. You can use either one based on your preference.'''
print(single_quote_multiline)